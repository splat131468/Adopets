package web.member.entity;

public class CreditCardVO implements java.io.Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer memID;
	private String cardNumber;
	private Integer defaultCreditCard;
	public Integer getMemID() {
		return memID;
	}
	public void setMemID(Integer memID) {
		this.memID = memID;
	}
	public String getCardNumber() {
		return cardNumber;
	}
	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}
	public Integer getDefaultCreditCard() {
		return defaultCreditCard;
	}
	public void setDefaultCreditCard(Integer defaultCreditCard) {
		this.defaultCreditCard = defaultCreditCard;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	@Override
	public String toString() {
		return "CreditCardVO [memID=" + memID + ", cardNumber=" + cardNumber + ", defaultCreditCard="
				+ defaultCreditCard + "]";
	}
	public CreditCardVO() {
		super();
	}

}
